-- Definisci la funzione per il calcolo della radice quadrata
function sqrt(x)
  return x^(0.5)
end

-- Definisci la funzione per il calcolo della potenza
function pow(x, y)
  return x^y
end

-- Definisci la funzione per il calcolo della radice cubica
function cbrt(x)
  return x^(1/3)
end

-- Definisci la funzione per il calcolo del logaritmo in base 10
function log10(x)
  return math.log(x, 10)
end

-- Definisci la funzione per il calcolo del logaritmo in base 2
function log2(x)
  return math.log(x, 2)
end

-- Definisci la funzione per il calcolo del logaritmo in base e (naturale)
function ln(x)
  return math.log(x)
end
