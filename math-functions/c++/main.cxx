#include <cmath>

// Calcola la radice quadrata di un numero
double sqrt(double x) {
    return std::sqrt(x);
}

// Calcola la potenza di un numero
double pow(double base, double exponent) {
    return std::pow(base, exponent);
}

// Calcola il valore assoluto di un numero
double abs(double x) {
    return std::abs(x);
}

// Calcola il seno di un angolo (in radianti)
double sin(double x) {
    return std::sin(x);
}

// Calcola il coseno di un angolo (in radianti)
double cos(double x) {
    return std::cos(x);
}

// Calcola il tangente di un angolo (in radianti)
double tan(double x) {
    return std::tan(x);
}
