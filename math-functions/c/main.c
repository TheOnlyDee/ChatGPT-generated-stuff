#include <stdio.h>
#include <math.h>

// Funzione per il calcolo della radice quadrata di un numero
double sqrt(double x) {
  return sqrt(x);
}

// Funzione per il calcolo della potenza di un numero
double pow(double x, double y) {
  return pow(x, y);
}

// Funzione per il calcolo del logaritmo di un numero
double log(double x) {
  return log(x);
}
